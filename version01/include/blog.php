            <h4><a href="#">Carmene</a></h4>
            <hr>
            <p class="intro">La carmenere es una variedad de uva plantada originalmente en la región
            de Médoc de Burdeos, Francia, donde es usada para producir vinos tintos oscuros y a 
            menudo es usada para vinos de mezcla con petit verdot.<br/>
            Es un miembro de la familia cabernet. El nombre Carmenere proviene de la palabra francesa
            carmín, por el color del foliage antes de la caída de la hoja. La uva también es conocida 
            como grande vidure, un sinónimo usado históricamente en Burdeos. La carmenere está 
            considerada una de las seis uvas tintas originarias de Burdeos, junto con la cabernet 
            sauvignon, la cabernet franc, la merlot, la malbec y la petit verdot.</p>
